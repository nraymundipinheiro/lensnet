# ──────────────────────────────────────────────────────────────────────────────
# Terminal Formatting
# ──────────────────────────────────────────────────────────────────────────────

TERMINALSIZE = 100


from tqdm import tqdm as _tqdm

_DEFAULTS = {
    "ncols": TERMINALSIZE,
    "dynamic_ncols": False,
    "bar_format": "{l_bar}{bar}| {n:4d}/{total:4d}"
}

def tqdm(*args, **kwargs):
    params = {**_DEFAULTS, **kwargs}
    return _tqdm(*args, **params)


